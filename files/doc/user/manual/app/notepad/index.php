<h1>Блокнот</h1>

<p>Запуск приложения <em>«Блокнот»</em> осуществляется через выбор соответствующего приложения в <em>«Главном меню»</em> или при помощи команды <em>notepad</em> в командной строке.
Приложение <em>«Блокнот»</em> представляет собой текстовый редактор с
возможностью сохранения файлов в формате txt. Сохранение файла
осуществляется по нажатию клавиши <em>F2</em>. При этом предлагается название
файла по умолчанию «Новый файл.txt» со стандартной директорией для
