Для сохранения файла с заданием определенной кодировки необходимо
нажать <em>F10</em>, согласиться с сохранением документа при помощи клавиши
«Y» и при помощи клавиш вверх/вниз выбрать нужную кодировку,
подтвердив выбор клавишей <em>Enter</em>.
Вновь открытый файл можно прочитать в другой кодировке также нажав клавишу <em>F10</em> и выбрав нужную кодировку.</p>
