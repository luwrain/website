                  <h2>What are we working on?</h2>
                  <p>
                    First of all, Luwrain is an operating system with an attempt to apply some new ideas and
                    any available experience in area of OS design for needs of blind persons. We guess we are
                    able to precisely describe the project philosophy with the following 10 brief statements:
                  </p>
                  <ol>
                    <li>It is an OS completely designed for blind and visually impaired persons.</li>
                    <li>It must be convenient, fast and clearly understandable.</li>
                    <li>It has to support various languages and localizations.</li>
                    <li>It aims to be sutable for desktop computers as well as for mobile work.</li>
                    <li>It is based on the Linux kernel and other FOSS software.</li>
                    <li>No any bias towards any particular hardware type.</li>
                    <li>It is non-profitable project.</li>
                    <li>
                      Its auditory is all users who need it. No matter what experience they have, what country
                      are they from, what profession are they occupied in and so on.
                    </li>
                    <li>
                      Everybody is able to participate in its development. but authors want to keep high quality
                      and urge everybody to respect accepted quality requirements for all incoming patches.
                    </li>
                    <li>
                      We believe available experience in GNU/Linux world is enough at least to start this work
                      and gives good base for success.
                    </li>
                  </ol>
                  <p>
                    Current time to do this work is very fortunate as Android, Tizen and FirefoxOS make it
                    is easy to realize that Linux turned out as a wonderful base for popular devices and in
                    same time it is available for everybody. Once you got proper understanding of LInux world
                    it becomes clear to you what you should do to create cutting-edge technology. We would
                    like to treat that fact as a good chance for solution of rather difficult problems as we
                    never had before.
                  </p>
