<h2>Новый невизуальный интерфейс</h2>

<p>LUWRAIN воплощает новую концепцию невизуального взаимодействия с&#160;ИТ,
которая значительно упрощает выполнение многих действий и существенно повышает скорость работы.
Вероятно, пользователю потребуется некоторое время, чтобы понять идею организации пространства LUWRAIN и привыкнуть к&#160;нему,
но мы уверены, что в конечном итоге каждый заинтересованный человек оценит преимущества нашего подхода.
В&#160; некоторых чертах LUWRAIN напоминает окружение <a href="http://emacspeak.sf.net">Emacspeak</a>,
но все идеи Emacspeak здесь претерпели очень глубокое переосмысление.</p>

<p>Всё рабочее пространство в&#160;LUWRAIN устроено как множество текстовых областей,
перемещение в&#160;которых осуществляется в&#160;стиле навигации по&#160;тексту&#160;&#x2014; самой привычной операции для&#160;людей с&#160;нарушениями зрения.
Некоторые элементы управления дополнены навигационными корректорами,
помогающими лучше чувствовать структуру рабочего объекта и повышающими информативность описания его&#160;состояния.
В результате в&#160;любом местоположении пользователю доступны следующие функции:</p>

<ol>
<li>Копирование содержимого элемента управления в буфер обмена целиком или любой его&#160;части, скажем, чтобы послать другу.
Обычный оконный интерфейс не&#160;позволяет выделять текст за&#160;пределами текущего элемента управления, что приводит к&#160;невозможности скопировать текст поля ввода вместе с&#160;его названием,
LUWRAIN не&#160;имеет подобных недостатков.
Выделение текста одинаково возможно в&#160;любом объекте,
будь то редактирование текста, окно терминала, фрагмент содержимого PDF-файла и&#160;т.&#160;д.</li>
<li>Поиск текста в&#160;текущем элементе управления.
Скажем, пользователь получил длинный вывод некоторой системной команды,
в&#160;LUWRAIN возможно быстро осуществить поиск выражения в&#160;нём,
если у&#160;него есть предположения о&#160;деталях полученного результата.
Начальная отметка выделения текста в&#160;LUWRAIN устанавливается отдельно от&#160;выбора конечной точки,
как это принято в&#160;GNU&#160;Emacs.
так что пользователь может найти отметку окончания выделения путём операции поиска.
В&#160;итоге становится неважно, насколько длинный фрагмент текста требуется скопировать&#160;&#x2014; эта&#160;операция выполняется одинаково легко при&#160;любом размере рабочего материала.</li>
<li>Содержимое элемента управления можно последовательно прослушать  сначала или с&#160;любой отметке в&#160;виде речевого описания.
В&#160;ходе чтения курсор будет следовать за&#160;диктором,
поэтому остановка чтения позволит быстро продолжить работу в&#160;новом положении.</li>
<li>Фрагмент текста под&#160;курсором может быть легко передан в&#160;сетевой сервис с&#160;прочтением результата обработки.
Пользователь неожиданно встретил новый термин&#160;&#x2014; не&#160;проблема,
комбинация клавиш Alt+G позволит осуществить запрос в&#160;поисковую систему <a href="http://duckduckgo/">DuckDuckGo</a>,
которая найдёт определение термина, а LUWRAIN прочитает результат.
Если пользователь подключит доступ к&#160;Яндекс.Переводчику,
любой фрагмент пользовательского пространства может быть переведён с&#160;иностранного языка.
Про&#160;интеграцию LUWRAIN с&#160;сетевыми сервисами рассказано дополнительно ниже.</li>
<li>Если курсор стоит на&#160;записи некоторой денежной суммы в&#160;иностранной валюте,
LUWRAIN легко переведёт её в&#160;национальную валюту и прочитает итоговую сумму.
Текущий обменный курс, конечно, будет запрошен автоматически в&#160;Интернете, в&#160;случае России напрямую в&#160;ЦБРФ.</li>
<li>Если пользователь выделит фрагмент мелодии в&#160;нотации <a href="http://lilypond.org">Lilypond</a> или фрагмент математической записи в&#160;нотации  <a href="https://ru.wikipedia.org/wiki/LaTeX">TeX</a>,
LUWRAIN проиграет выделенную мелодию или прочитает математическую запись на&#160;естественном языке.
Это открывает возможность к&#160;ведению повсеместных заметок и рабочих материалов для&#160;самых творческих людей!
Про&#160;использование Lilypond и TeX также рассказано ниже.</li>
</ol>

<p>LUWRAIN расширяет возможности звукового вывода благодаря получению расширенной информации о&#160;рабочем пространстве пользователя.
Мы применяем, как их&#160;назвали, контекстные звуки, которые в&#160;ненавязчивой форме уведомляют пользователя,
что система ожидает ввод в&#160;всплывающем окне или что она сейчас занята сетевым обменом.
LUWRAIN стремится задействовать как можно больше способов управления компьютером.
В&#160;частности, в&#160;GNU/Linux пользователь способен управлять системой при&#160;помощи <a href="https://ru.wikipedia.org/wiki/%D0%A2%D0%B0%D1%87%D0%BF%D0%B0%D0%B4">тачпада</a>,
не&#160;привязываяс при&#160;этом к&#160;каким-либо пространственным координатам объекта.
Мы также работаем над&#160;тем, чтобы речевой вывод,
который получает пользователь,
максимально приближался к&#160;естественному языку,
а не&#160;повторял формальные записи на&#160;экране.</p>

