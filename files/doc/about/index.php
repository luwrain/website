<h2>Что такое LUWRAIN?</h2>

<p>Наш&#160;проект LUWRAIN  посвящён разработке ПО в&#160;сфере  невизуального использования информационных технологий (ИТ).
Здесь мы подразумеваем все&#160;случаи, когда экран ПК или мобильного устройства  не&#160;является основным способом передачи данных.
Работа в&#160;проекте LUWRAIN разбивается на&#160;несколько крупных направлений:</p>

<ol>
<li>Разработка основной платформы на&#160;Java для&#160;создания и распространения невизуальных приложений.</li>
<li>Разработка двух семейств приложений для&#160;нашей платформы:
<ul>
<li>приложения общего назначения для&#160;повседневной работы, включая доступ к&#160;популярным сервисам в&#160;интернете;</li>
<li>образовательные приложения для&#160;детей и студентов с&#160;нарушениями зрения.</li>
</ul></li>
<li>Исследование методологии построения невизуального интерфейса и семантических структур для&#160;голосового управления информационными объектами.</li>
<li>Разработка технологий для&#160;быстрой и дешёвой адаптации материалов, прежде всего учебных,  для&#160;представления в&#160;невизуальной форме.</li>
<li>Создание учебной литературы в&#160;формате Daisy по&#160;заказу издательств и образовательных учреждений.</li>
</ol>

<p>Хотя цели нашего проекта не&#160;ограничиваются вспомогательными  технологиями,
создание инструментов для&#160;людей с&#160;нарушениями зрения является очень важным направлением LUWRAIN.
По&#160;косвенным оценкам только 1%  людей с&#160;нарушениями зрения в&#160;России чувствуют себя свободно в&#160;работе на&#160;ПК,
LUWRAIN призван улучшить эту&#160;ситуацию, делая ИТ более дружественными и комфортными в&#160;режиме невизуального взаимодействия.
Мы также активно поддерживаем проведение исследований в&#160;сфере клинической психологии,
чтобы лучше понять природу проблем мотивации и социализации  детей и взрослых людей с&#160;нарушениями зрения.</p>

<p>В&#160;настоящий момент возможны только два&#160;способа осуществления образовательного процесса людей с&#160;нарушениями зрения: методики на&#160;основе алфавита Брайля и при&#160;помощи ИТ.
Алфавит Брайля остаётся надёжным инструментом, но не&#160;отвечает всем современным требованиям.
Мы уделяем большое внимание подготовке образовательных приложений и материалов, а&#160;также
внедрению и распространению издательских систем, таких как Latex и Lilypond,
которые незаслуженно остаются в&#160;стороне от&#160;внимания большинства людей.
Мы были&#160;бы очень рады установлению контактов с&#160;крупными  организациями
с&#160;целью проведения исследований возможности трудоустройства в&#160;них людей с&#160;нарушениями зрения
и целенаправленной подготовке для&#160;этого  специалистов в&#160;ВУЗах, с&#160;которыми мы сотрудничаем.</p>

<p>LUWRAIN&#160;&#x2014; это не&#160;только ИТ, это также дизайн и стиль жизни.
