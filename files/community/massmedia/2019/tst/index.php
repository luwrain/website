<p>Источник: <a href="https://te-st.ru/2019/07/10/mikhail-pozidaev-about-what-inclusion-not-work/">https://te-st.ru/2019/07/10/mikhail-pozidaev-about-what-inclusion-not-work/</a></p>

<p>IT-индустрия в интересном положении&#160;&#x2014; это технологический социальный тупик. Для незрячих есть
адаптивные технологии и специальные программы обучения. Проблема первых&#160;&#x2014; исключительно
формальная доступность, а вторых&#160;&#x2014; оторванность от реальности. Я уверен, что ситуацию можно и
нужно менять. Над этим мы с коллегами работаем уже несколько лет.</p>

<p>Объясню на примере. С одной стороны, в IT-продуктах, например в Windows, достаточное количество
функций, которыми могут пользоваться незрячие. Физически возможность нажать на кнопку, не глядя
на нее, есть. С другой стороны, эта доступность сама по себе мало полезна, когда нужно решать
комплексные технические задачи.</p>

<p>Еще пара примеров: для незрячих создан нотный редактор GNU LilyPond&#160;&#x2014; если почитать, как он
работает, можно понять, почему им пользуется буквально несколько технически продвинутых
специалистов. В сфере образования есть интерактивные приложения, но сфера их применения
сильно ограничена. Можно сделать тифлоцифровую таблицу Менделеева, но попробуйте подобным
образом объяснить незрячему устройство кровеносной системы?</p>

<p>Программирование&#160;&#x2014; отдельная история. Для человека, мало знакомого с IT, все выглядит просто:
пишешь текст, компьютер его обрабатывает, и вот он результат. Но на деле все сложнее&#160;&#x2014; это и
вычислительная математика, и графические алгоритмы, и «упаковка» информации… Чтобы освоить
эти навыки, слабовидящим нужны специальные устройства и, конечно, помощь специалистов.</p>

<p>Работа в IT&#160;&#x2014; это комплексный процесс и растущий поток задач: просто знать кнопки недостаточно.
Между физической доступностью и практической применимостью настоящая пропасть. Главный
недостаток технических решений для слабовидящих&#160;&#x2014; недостаточная функциональность. Чтобы
решить эту проблему, мы создали платформу Luwrain. В команде проекта, помимо меня, есть
специалисты Алексей Голошумов и Елена Теплых.</p>

<pre><code>Luwrain позволяет работать с большинством текстовых документов, изучать страницы в
Интернете и общаться в Сети, не используя зрение. Философия платформы&amp;nbsp;&amp;mdash; гарантированная
доступность. Если возникает вопрос, можно ли через Luwrain решить какую-либо техническую
задачу, ответ прост: «да».
</code></pre>

<p>Это написанная на Java пользовательская среда, в которой задействованы невизуальные способы
взаимодействия человека с компьютером. Обратная связь осуществляется голосом или при
необходимости через брайлевский дисплей. Одно из приложений платформы без установки Office
«понимает» txt, html, doc, docx, xlsx и без обратной связи читает html и pdf. То есть Luwrain в этом
смысле самодостаточен. Это достигается за счет множества готовых библиотек, созданных для
Java.</p>

