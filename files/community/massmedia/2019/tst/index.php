<h2>Как масштабировать опыт Luwrain</h2>

<p>Поскольку платформа Luwrain бесплатная, многие интересуются вопросом финансирования
проекта. История интересная. Однажды я познакомился с коллегой из Китая. Он основал в
Шэньчжэне, Китай, компанию, которая занимается видеомонтажом. Особенность фирмы в том, что все
сотрудники&#160;&#x2014; люди с ограниченными возможностями, в основном с нарушениями
опорно-двигательного аппарата. Они занимаются постпродакшн&#160;&#x2014; обрабатывают видео, работая с
заказчиками по всему миру. Им отдают задачи, на которые компании не хотят тратить ни время, ни
ресурсы. Получилась такая работающая экономическая модель, которую я захотел взять на
вооружение.</p>

<p>По возвращении в Россию на базе нашей компании мы начали создавать подобную систему выполнения
заказов. В корпоративной среде масса рутинной работы, на выполнении которой можно сэкономить
силы и деньги&#160;&#x2014; от сортировок заявок колл-центра до верификации переводов. Мы привлекаем
слабовидящих людей к выполнению подобных задач&#160;&#x2014; получается стратегия win win. Для нас это самое
эффективное коммерческое направление, потому что гранты&#160;&#x2014; это разовая помощь (которую к тому же
не так просто получить), а привлечение инвестиций требует выстроенной стратегии монетизации.
Парадокс в том, что социальная значимость проекта не ставит его в выигрышное положение в
бизнесе.</p>

<p>Сейчас у нас две проблемы, которые можно решить, лишь наладив сотрудничество со сторонними
специалистами. Первая&#160;&#x2014; стратегическая, вторая&#160;&#x2014; информационная.</p>

<p>С точки зрения стратегии, нужно ответить на вопросы, какие научные сферы мы должны задействовать
для развития (перспективы применения платформы достаточно широкие, но мы пока сосредоточены
только на образовании), в какие сроки на какие результаты можно рассчитывать (именно такие
вопросы о масштабировании задают нам инвесторы) и, наконец, какой общий эффект от применения
платформы (это важно в общегосударственном масштабе, поскольку поддержка нам нужна
всесторонняя).</p>

<p>Нам необходима компетентная стратегическая оценка: очертить пул задач и определить
необходимые ресурсы для их решения. Я знаю, что образовательная платформа легко превращается
в успешную коммерческую модель, но практики в данном вопросе нет&#160;&#x2014; нужна помощь.</p>

<p>Вторая проблема, информационная, как раз затрудняет решение первой. У нас большая страна, не
имеющая выстроенных связей с незрячими людьми. Как нам рассказать о проекте, заинтересовать
в изучении платформы, наладить сотрудничество с регионами? Условно для слабовидящих есть три
сайта, но их аудитория крайне мала и пассивна.</p>

<p>Между тем у незрячих огромный потенциал. Вспомните Джона Мильтона, диктовавшего свой
«Потерянный Рай» (будучи слепым, английский поэт диктовал текст своим друзьям и дочери.&#160;&#x2014; Прим.
ред.), или Гомера&#160;&#x2014; половина найденных древнегреческих литературных папирусов&#160;&#x2014; отрывки из
